                    <p>দেশের প্রথম স্যাটেলাইট বঙ্গবন্ধু-১ স্যাটেলাইটের গায়ে নিজ হাতে ‘জয় বাংলা জয় বঙ্গবন্ধু’ শ্লোগান লিখে দিয়েছেন তথ্য প্রতিমন্ত্রী তারানা হালিম।  এছাড়া তিনি জানিয়েছেন, স্যাটেলাইটটিতে তার নিজের লেখা শ্লোগান আর বাংলাদেশ সরকারের একটা লোগো ছাড়া আর কিছু নেই।
তারানা হালিম জানান, “আমি যখন টেলিযোগাযোগ বিভাগের প্রতিমন্ত্রী হিসেবে স্যাটেলাইটটি নির্মাণ শেষে দেখতে যাই তখন ‘থ্যালেস অ্যালেনিয়া’ আমাকে বলে স্যাটেলাইটের গায়ে কিছু একটা লিখে সই করতে। তখন আমি ‘জয় বাংলা জয় বঙ্গবন্ধু’ হাতে লিখে দেই। সেই লেখাটি স্যাটেলাইটের গায়ে থেকেছে। যেখানে মানুষ পৌঁছাতে পারে না, যা অসীম মহাশূন্যেও পৌঁছে যাবে বঙ্গবন্ধুর নাম।” </p>
